#include <unistd.h>

size_t	ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = -1;
	if (s)
	{
		while (++i < ft_strlen(s))
			ft_putchar_fd(s[i], fd);
	}
}
